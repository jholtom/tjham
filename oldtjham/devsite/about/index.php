<div class="hilt">
As amateur radio operators, we can talk to fellow "hams" around the world using HF frequencies similar to those that AM radio uses. We also learn about radio propagation, antennas, solar cycles, and electronics. Another key part of amateur radio is service. Hams often assist in events, such as the Marine Corps Marathon, providing backup communications when other forms of communication, such as phones and cell phones, are disabled.</div><br /><br />
<div class="content">
<div class="hrule">Meetings</div>
General Club meetings are held 8th period A blocks on Wednesdays. We usually meet in room 124, and from there go to the Shack. Occasionally we use room 118S to work. Anyone is welcome to our meetings anytime. A student is usually considered a member of the club after showing up to three meetings; see the club constitution for specific details (link below). Meetings are generally laid back, and we're usually either building something, fixing something, or just talking.<br /><br />
<div class="hrule">Club Station</div>
We operate a small station from the Shack. We use the large antenna (visible from the front of the school) in the courtyard. We're working on a club license; as of now we use individual callsigns when talking on club equipment. You must have a proper license in order to use club equipment, or have a licensed individual with you.<br /><br />

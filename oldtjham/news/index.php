<h3>Sunday, November 13, 2008</h3>
<p>
We will have an antenna event today similar to last year's to replace 
the antenna that was struck by lightning over the summer. 
</p>
<h3>Tuesday, September 23, 2008</h3>
<p>
Our first meeting of this school year is tomorrow, Wednesday, September 24.
</p>
<h3>Tuesday, June 3, 2008</h3>
<p>
We've received a frequency for our repeater through T-MARC. We're almost
done setting up the controller and purchasing the remaining parts necessary
for the repeater. We'll be looking forward to launching our repeater in the
fall of 2008.
</p>


<h3>Sunday, December 2, 2007</h3>
<p>
Congratulations on a successful Antenna Party at TJ today! We were able to 
put up our future repeater antenna on the tower. Special thanks to everyone
who came and to our tower climber, Mr. John Birch.
</p>

